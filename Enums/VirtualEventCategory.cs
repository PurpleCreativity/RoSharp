namespace RoSharp.Enums
{
    /// <summary>
    /// Represents the category of a virtual event.
    /// </summary>
    public enum VirtualEventCategory
    {
        /// <summary>
        /// New content added to the game.
        /// </summary>
        NewContent,

        /// <summary>
        /// Items available for a limited time.
        /// </summary>
        ItemDrop,

        /// <summary>
        /// New experience season.
        /// </summary>
        NewSeason,

        /// <summary>
        /// New location added to the experience.
        /// </summary>
        NewLocation,

        /// <summary>
        /// New map added to the experience.
        /// </summary>
        NewMap,

        /// <summary>
        /// More levels added to the experience.
        /// </summary>
        MoreLevels,

        /// <summary>
        /// New features added to the experience.
        /// </summary>
        NewFeature,

        /// <summary>
        /// Early access release for unfinished content.
        /// </summary>
        EarlyAccess,

        /// <summary>
        /// Expansion of a pre-existing location or map.
        /// </summary>
        Expansion,

        /// <summary>
        /// Limited-time experience challenges.
        /// </summary>
        Challenge,

        /// <summary>
        /// Limited-time experience quests.
        /// </summary>
        Quest,

        /// <summary>
        /// Limited-time experience gathering/festival.
        /// </summary>
        Festival,
    }
}
